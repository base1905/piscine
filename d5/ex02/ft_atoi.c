int		ft_atoi(char *str)
{
	int i;
	int k;
	int res;

	k = 0;
	res = 0;
	i = 0;
	while ((str[k] >= '\t' && str[k] <= '\r') || str[k] == ' ')
	{
		k++;
	}
	if (str[k] == '-' || str[k] == '+')
	{
		k++;
	}
	i = k;
	while (str[i] >= '0' && str[i] <= 57)
	{
		res = res * 10 + (str[i] - 48);
		i++;
	}
	if (str[k - 1] == '-')
		res *= -1;
	return (res);
}
