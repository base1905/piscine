#include "libf.h"

void	ft_null(int i, int j, int nb, char ***desk)
{
	int		ii;
	int		jj;

	ii = 0;
	while (ii < 9)
	{
		desk[i][ii][nb] = 0;
		desk[ii][j][nb] = 0;
		ii++;
	}
	ii = (i / 3) * 3;
	jj = (j / 3) * 3;
	while (ii >= ((i /	3) * 3)  && ii < (((i / 3) * 3) + 3))
	{
		jj = 0;
		while (jj >= ((j / 3) * 3) && jj < (((j / 3) * 3) + 3))
		{
			desk[ii][jj][nb] = 0;
			jj++;
		}
		ii++;
	}
}
