#include "libf.h"

int		ft_sum(int i, int j, char ***desk)
{
	int		k;
	int		sum;

	sum = 0;
	k = 1;
	while (k < 10)
	{
		sum += desk[i][j][k];
		k++;
	}
	return (sum);
}
