void	ft_null_all(char ***desk)
{
	int		i;
	int		j;
	int		k;

	i = 0;
	k = 1;
	j = 0;
	while (i < 9)
	{
		j = 0;
		while (j < 9)
		{
			k = 1;
			while (k < 10)
			{
				desk[i][j][k] = 0;
				k++;
			}
			j++;
		}
		i++;
	}
}
